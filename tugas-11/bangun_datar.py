def persegi(sisi1,sisi2):
    hitung = sisi1*sisi2
    print("Luas Persegi =",hitung,"cm^2")
def persegi_panjang(panjang,lebar):
    hitung = panjang*lebar
    print("Luas Persegi Panjang =",hitung,"cm^2")
def Lingkaran(r):
    hitung = 22/7*r*r
    print("Luas Lingkaran =",hitung,"cm^2")
def segitiga(alas,tinggi):
    hitung = (1/2)*alas*tinggi
    print("Luas Segitiga =",hitung,"cm^2")
def trapesium(sisi_atas,sisi_bawah,tinggi):
    hitung = (1/2)*(sisi_atas+sisi_bawah)*tinggi
    print("Luas Trapesium =",hitung,"cm^2")